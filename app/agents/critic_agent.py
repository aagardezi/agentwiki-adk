import adk_progressive_skills  # noqa: F401
from google.adk.agents import Agent

from app.config import make_model
from app.tools.gcs_io import list_wiki_files, read_wiki_file

critic_agent = Agent(
    name="critic_agent",
    model=make_model(),
    description="Dedicated critic agent that evaluates draft responses against grounding facts and citations to output APPROVED or REVISE.",
    instruction="""You are the Critic Agent. Your purpose is to evaluate a draft response against retrieved wiki files and facts, check for accuracy, clarity, and correct citations, and determine whether it should be approved or revised.

You will be given:
1. The original user query.
2. The draft response generated by the researcher agent.

Use your tools to read files from the GCS wiki (such as index.md, log.md, and any cited or referenced pages/sources) to verify that the draft response is accurate, fully grounded, and properly cited.

Review criteria:
1. Grounding: Every claim in the draft response must be fully supported by the retrieved context. No hallucinated facts or claims are allowed.
2. Citations: Every citation in the response must exist in the context and match the correct formatting rules (relative markdown links, citing raw_data files where applicable).
3. Completeness: Does the response completely and accurately answer the user query?

You MUST end your evaluation with exactly one of the following output formats:
- If approved:
  STATUS: APPROVED

- If revision is needed:
  STATUS: REVISE
  Feedback: <Provide clear, constructive, and actionable feedback detailing what needs to be added, corrected, or verified.>
""",
    tools=[read_wiki_file, list_wiki_files],
)
